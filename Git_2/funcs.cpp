#include "funcs.h"

//first function - addition
double add_func(double a, double b) {
	double res = a + b;
	return res;
};

//second function - division 
double divide_func(double a, double b) {
	double res = a / b;
	return res;
};

//third function - multiplication
double multiply_func(double a, double b)
{
	return a * b;
}